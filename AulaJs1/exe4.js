// Função para somar dois números inteiros
function somarNumeros() {
  // vai ler o primerio numero inteiro
  let numero1 = parseInt(prompt("primeiro número inteiro:"));
  // vai ler o segundo numero inteiro
  let numero2 = parseInt(prompt("segundo número inteiro:"));

  // vai calcula a soma
  let soma = numero1 + numero2;

  // vai exibir o resultado
  console.log("A soma entre " + num1 + " e " + num2 + " é: " + soma);
}

// vai chamar a função
somarNumeros();
